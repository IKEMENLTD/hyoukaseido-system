/**
 * 現在のパスに対応するヘルプデータを取得する。
 * 長いパスから順にプレフィックスマッチを行い、最も具体的なヘルプを返す。
 */
export function getHelpForPath(pathname: string): PageHelp | null {
  // 完全一致を先に試す
  if (helpData[pathname]) return helpData[pathname];

  // プレフィックスマッチ（長い順）
  const keys = Object.keys(helpData).sort((a, b) => b.length - a.length);
  for (const key of keys) {
    if (pathname.startsWith(key)) return helpData[key];
  }

  return null;
}
